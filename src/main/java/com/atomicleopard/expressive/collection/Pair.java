package com.atomicleopard.expressive.collection;

/**
 * <p>
 * {@link Pair} describes a simple tuple.
 * </p>
 * <p>
 * This is useful in scenarios where two items need to be grouped, or a method requires two return values. In these scenarios, Pair reduces code bloat by avoiding the creation of small simple DTO
 * classes.
 * </p>
 * 
 * @param <A>
 * @param <B>
 */
public class Pair<A, B> {
	private A a;
	private B b;

	public Pair(A a, B b) {
		super();
		this.a = a;
		this.b = b;
	}

	public A getA() {
		return a;
	}

	public B getB() {
		return b;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((a == null) ? 0 : a.hashCode());
		result = prime * result + ((b == null) ? 0 : b.hashCode());
		return result;
	}

	@SuppressWarnings("rawtypes")
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Pair other = (Pair) obj;
		if (a == null) {
			if (other.a != null)
				return false;
		} else if (!a.equals(other.a))
			return false;
		if (b == null) {
			if (other.b != null)
				return false;
		} else if (!b.equals(other.b))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return String.format("Pair of %s and %s", a, b);
	}
}
